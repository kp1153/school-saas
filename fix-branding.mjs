// fix-branding.mjs
import { readFileSync, writeFileSync, readdirSync, statSync } from "fs";
import { join } from "path";

const replacements = [
  [">EduSaaS</div>", ">निशांत स्कूल</div>"],
  ["EduSaaS School", "निशांत स्कूल"],
  ["EduSaaS — Student Portal", "निशांत स्कूल — छात्र पोर्टल"],
  ["Generated by EduSaaS", "निशांत स्कूल सॉफ्टवेयर"],
  ["— EduSaaS School", "— निशांत स्कूल"],
  ["School Management Platform", "विद्यालय प्रबंधन सॉफ्टवेयर"],
  [">Parent Portal</div>", ">अभिभावक पोर्टल</div>"],
  [">Student Portal</div>", ">छात्र पोर्टल</div>"],
];

function walk(dir) {
  const files = [];
  for (const f of readdirSync(dir)) {
    const full = join(dir, f);
    if (statSync(full).isDirectory()) files.push(...walk(full));
    else if (f.endsWith(".js")) files.push(full);
  }
  return files;
}

walk("app").forEach((file) => {
  let content = readFileSync(file, "utf8");
  let changed = false;
  replacements.forEach(([from, to]) => {
    if (content.includes(from)) {
      content = content.replaceAll(from, to);
      changed = true;
    }
  });
  if (changed) {
    writeFileSync(file, content, "utf8");
    console.log("✅ Fixed:", file);
  }
});

console.log("Done.");